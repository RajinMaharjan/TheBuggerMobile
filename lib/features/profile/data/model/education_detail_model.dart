class EducationDetailModel {
  late final String education;
  late final String university;
  late final String course;
  late final String specialization;
  late final String startYear;
  late final String endYear;
  late final String gradingSystem;
  late final String grade;

  EducationDetailModel({
    required this.education,
    required this.university,
    required this.course,
    required this.specialization,
    required this.startYear,
    required this.endYear,
    required this.gradingSystem,
    required this.grade,
  });
}
